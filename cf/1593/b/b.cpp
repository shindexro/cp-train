void solve(int tc)
{
    string n;
    cin >> n;
    int last = n.size() - 1;
    int zero = n.rfind('0');
    int five = n.rfind('5');

    int idx = -1;
    if (zero > 0) {
        int i = n.rfind('0', zero - 1);
        if (i != -1)
            idx = max(idx, i);
        i = n.rfind('5', zero - 1);
        if (i != -1)
            idx = max(idx, i);
    }
    if (five > 0) {
        int i = n.rfind('2', five - 1);
        if (i != -1)
            idx = max(idx, i);
        i = n.rfind('7', five - 1);
        if (i != -1)
            idx = max(idx, i);
    }

    cout << last - idx - 1 << "\n";
}

