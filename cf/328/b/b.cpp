void solve(int tc)
{
    string t, s;
    cin >> t >> s;

    int freq[10] = { 0 };
    for (char c : s) {
        freq[c - '0']++;
    }

    int per[10] = { 0 };
    for (char c : t) {
        per[c - '0']++;
    }

    freq[6] += freq[9];
    freq[9] = 0;
    per[6] += per[9];
    per[9] = 0;
    freq[2] += freq[5];
    freq[5] = 0;
    per[2] += per[5];
    per[5] = 0;

    int ans = INT_MAX;
    for (int i = 0; i < 10; i++)
        if (per[i])
            ans = min(ans, freq[i] / per[i]);
    cout << ans << "\n";
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    int T = 1;
    for (int t = 1; t <= T; t++)
        solve(t);
}
