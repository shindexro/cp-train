void solve(int tc)
{
    int n;
    cin >> n;
    vector<int> v(n);
    for (int& i : v)
        cin >> i;

    vector<int> w(v);
    sort(all(w));

    map<int, int> odd_freq, even_freq;
    for (int i = 0; i < n; i++) {
        if (i & 1) {
            odd_freq[v[i]]++;
        } else {
            even_freq[v[i]]++;
        }
    }

    for (int i = 0; i < n; i++) {
        if (i & 1) {
            if (!odd_freq[w[i]]) {
                cout << "NO\n";
                return;
            }
            odd_freq[w[i]]--;
        } else {
            if (!even_freq[w[i]]) {
                cout << "NO\n";
                return;
            }
            even_freq[w[i]]--;
        }
    }
    cout << "YES\n";
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    int T;
    cin >> T;
    for (int t = 1; t <= T; t++)
        solve(t);
}
