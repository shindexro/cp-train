void solve(int tc)
{
    int n;
    cin >> n;
    vector<int> v(n + 1);
    for (int i = 1; i <= n; i++)
        cin >> v[i];

    vector<int> dp(n + 1);
    dp[1] = 1;
    for (int i = 2; i <= n; i++) {
        dp[i] = 1;
        for (int f = 1; f * f <= i; f++) {
            if (i % f == 0 && v[i] > v[f]) {
                dp[i] = max(dp[i], dp[f] + 1);
            }
            int f2 = i / f;
            if (i % f2 == 0 && v[i] > v[f2]) {
                dp[i] = max(dp[i], dp[f2] + 1);
            }
        }
    }
    debug(dp);
    cout << *max_element(all(dp)) << "\n";
}

