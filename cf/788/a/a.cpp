    cin >> n;
    vector<ll> v(n);
    for (ll& a : v)
        cin >> a;

    vector<ll> diff;
    for (int i = 0; i < n - 1; i++) {
        diff.push_back(abs(v[i] - v[i + 1]));
    }

    vector<ll> dp_pos(n), dp_neg(n);
    for (int i = 1; i < n; i++) {
        dp_pos[i] = max(dp_neg[i - 1] + diff[i - 1], diff[i - 1]);
        dp_neg[i] = dp_pos[i - 1] - diff[i - 1];
    }
    debug(diff);
